package com.company;

public class Fish {
    private String name;
    private String color;
    private int age;
    private int length;

    public void setName(String name) {
        this.name = name;
    }

     public String getName() {
        return name;
    }

    public void setColor(String color) {
        this.color = color;
    }

    public String getColor() {
        return color;
    }

    public void setAge(int age) {
        this.age = age;
    }

    public int getAge() {
        return age;
    }

    public void setLength(int length) {
        this.length = length;
    }

    public int getLength() {
        return length;
    }


}
